    //The user has clicked to pay his taxes early
    private void payTaxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payTaxActionPerformed
        if(Game.money.compareTo(Game.government.taxValue) >= 0){
            Game.GiveMoney(Game.government.taxValue.negate());
            Game.government.ChangeTaxTime(180);
        }else{
            Announcements.DisplayAnnouncement("Você não tem dinheiro o suficiente para pagar o imposto!", Announcements.TextStyles.CriticalAlert);
        }
    }//GEN-LAST:event_payTaxActionPerformed

    private void killLeftistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_killLeftistActionPerformed
        //Kill a leftist if the player has the money
        if(Game.money.compareTo(Game.government.killLeftCost) >= 0 && Game.government.leftists > 0){
            Game.government.AddLeftist(-1);
        }else{
            //Display announcement if there is no leftist
            if(Game.government.leftists == 0)
                Announcements.DisplayAnnouncement("Por enquanto não existe nenhum esquerdista vivo!", Announcements.TextStyles.CriticalAlert);
            //Display announcement if the player doesn't have money
            else
                Announcements.DisplayAnnouncement("Você não tem dinheiro o suficiente para matar 1 esquerdista!", Announcements.TextStyles.CriticalAlert);
        }
            
    }//GEN-LAST:event_killLeftistActionPerformed

    private void destroyGovernmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_destroyGovernmentActionPerformed
        if(Game.money.compareTo(Game.government.takedownCost) >= 0){
            //Deactivate the government
            Game.government.DeactivateGovernment();
            
            //Take the money
            Game.GiveMoney(Game.government.takedownCost.negate());
            
            //Display announcement
            Announcements.DisplayAnnouncement("Você derrubou o governo! Um movimento esquersista está em ascensão!", Announcements.TextStyles.GoodAlert);
        } else
            Announcements.DisplayAnnouncement("Você não tem dinheiro o suficiente para derrubar o governo", Announcements.TextStyles.CriticalAlert);
        
    }//GEN-LAST:event_destroyGovernmentActionPerformed

    //The user has changed an item from the multiplier
    private void potatoMultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_potatoMultActionPerformed
        //Change the multiplier depending the setting that the player has defined
        switch(potatoMult.getSelectedItem()+""){
            case "1x":
            multiplier = 1;

            break;
            case "10x":
            multiplier = 10;

            break;
            case "100x":
            multiplier = 100;

            break;
            case "1000x":
            multiplier = 1000;

            break;
        }

        //Update UI
        for(Potatoes potato : Potatoes.allPotatoes){
            potato.potatoUi.potatoPriceUI.setText(Utils.stringifyBD(potato.currentPrice.multiply(new BigDecimal(multiplier)))+"");
        }
    }//GEN-LAST:event_potatoMultActionPerformed

