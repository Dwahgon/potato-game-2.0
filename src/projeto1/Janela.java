    //Fills the inventory
    void FillShop(){
        //Table to store all of the items inventory UI
        List<JComponent> allItemsShopUI = new ArrayList<>();
        
        //Get all of the inventory UI of the items and store them on the table
        for (Item item : Item.allItems) {
            if (item.shopUI != null) {
                allItemsShopUI.add(item.shopUI);
            }
        }
        
        //Convert List to regular Array
        JComponent[] jcompArray = new JComponent[allItemsShopUI.size()];
        jcompArray = allItemsShopUI.toArray(jcompArray);
        
        //Create the grid layout for the inventory
        CreateGridLayout(jPanel12, jcompArray , 3, 25, 10, 20, 10);
    }
    
