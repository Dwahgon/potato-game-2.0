    //Creates a grid
    private void CreateGridLayout(JComponent gridComp, JComponent[] gridItems, int qntRow, int startXGap, int startYGap, int itemsGapX, int itemsGapY){
        //Set the layout of the component that we will be filling
        gridComp.setLayout(null);
        
        int gridCompHeight = 0;
        int gridCompWidth = 0;
        for (int i = 0; i < gridItems.length; i++) {
            JComponent gItem = gridItems[i];
            
            //Add the grid item to the grid
            gridComp.add(gItem);

            //Define the X and Y position of the gridItem
            int x = startXGap + (i % qntRow * gItem.getWidth()) + (i % qntRow * itemsGapX);
            int y = startYGap + (i / qntRow * gItem.getHeight()) + (i / qntRow * itemsGapY);
            
            //Set the layout
            gItem.setLocation(x, y);
            
            //Set the height for the grid component
            if(gridCompHeight < gItem.getY() + gItem.getHeight())
                gridCompHeight = gItem.getY() + gItem.getHeight();
            
            //Set the width of the grid component
            if(gridCompWidth < gItem.getX() + gItem.getWidth())
                gridCompWidth = gItem.getX() + gItem.getWidth();
        }
        
        //Set the size of the inner frame
        gridComp.setPreferredSize(new Dimension(gridCompWidth, gridCompHeight));
    }
    
    //Creates a production stat label
    private JLabel CreateStatusLabel(String statName, String statValue, Rectangle statNameRec, Rectangle statValueRec){
        //Create JLabel
        JLabel productionLabel = new JLabel();
        JLabel production = new JLabel();

        //Set the texts
        productionLabel.setText(statName);
        productionLabel.setToolTipText(statName);
        production.setText(statValue);

        //Add the UI to the panel
        jPanel4.add(productionLabel);
        jPanel4.add(production);

        //Set them visible
        productionLabel.setVisible(true);
        production.setVisible(true);

        //Set the position and size
        productionLabel.setBounds(statNameRec);
        production.setBounds(statValueRec);
        
        return production;
    }
    
